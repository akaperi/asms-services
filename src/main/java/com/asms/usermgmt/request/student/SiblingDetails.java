package com.asms.usermgmt.request.student;

/*
 * ParentDetails is the mapping class
 * to get parent details from front end
 * 
 * 
 */

public class SiblingDetails {

	private String name;

	private int age;

	private String gender;
	
	private String siblingClass;
	
	private String school;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getSiblingClass() {
		return siblingClass;
	}

	public void setSiblingClass(String siblingClass) {
		this.siblingClass = siblingClass;
	}

	public String getSchool() {
		return school;
	}

	public void setSchool(String school) {
		this.school = school;
	}
	
	
	
	

}
